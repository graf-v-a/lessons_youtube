# Задача 3: Таблица умножения
# Уровень: Средний
# Цель: Циклы for, операторы, вывод
#
# Условие:
# Пользователь вводит число. Нужно вывести таблицу умножения для этого числа от 1 до 10.
number = int(input("Введите число: "))
print(f"Таблица умножения для числа {number}:")
for i in range(1, 11):
    print(f"{number} x {i} = {number * i}")