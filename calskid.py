# Задача 1: Калькулятор скидки (простая)
# Описание: Напиши программу, которая запрашивает у пользователя стоимость товара (число) и процент скидки (число).
# Программа должна вычислить итоговую стоимость товара после скидки и вывести результат в формате: "Итоговая стоимость: {сумма} рублей".

price = float(input("Введите стоимость товара: "))
discount = float(input("Введите процент скидки: "))
final_price = price - (price * discount / 100)
print(f"Итоговая стоимость: {final_price:.2f} рублей")