# Задача 2: Проверка пароля (средняя)
# Описание: Напиши программу, которая запрашивает у пользователя пароль и проверяет, соответствует ли он следующим требованиям:
#
# Длина пароля должна быть не менее 8 символов.
# Пароль должен содержать хотя бы одну цифру (0–9).
# Если пароль соответствует требованиям, выведи "Пароль принят".
# Если нет, укажи, какое требование не выполнено.
# Пользователь может пытаться ввести пароль до тех пор, пока не введет правильный или не введет слово "выход".


password = input("Введите пароль (или 'выход' для выхода): ")
while password != "выход":
    if len(password) < 8:
        print("Пароль должен содержать не менее 8 символов.")
    elif not any(char.isdigit() for char in password):
        print("Пароль должен содержать хотя бы одну цифру (0-9).")
    else:
        print("Пароль принят.")
        break
    password = input("Попробуйте еще раз (или 'выход' для выхода): ")