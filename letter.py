# Задача 3: Подсчет слов в тексте (сложная)
# Описание: Напиши программу, которая запрашивает у пользователя строку текста и подсчитывает количество слов, начинающихся с определенной буквы (букву также запрашивает пользователь).
# Слова разделяются пробелами. Программа должна игнорировать регистр букв (например, "А" и "а" считаются одинаковыми).
# Выведи результат в формате: "Слов на букву {буква}: {количество}".

text = input("Введите строку текста: ")
letter = input("Введите букву: ").lower()
words = text.split()
count = 0
for word in words:
    if word.lower().startswith(letter):
        count += 1
print(f"Слов на букву '{letter}': {count}")
# Задача 4: Словарь синонимов (сложная)
# Описание: Напиши программу, которая создает словарь синонимов. Пользователь вводит слово и его синоним. Программа должна запрашивать слова и их синонимы до тех пор, пока пользователь не введет "стоп".
# После ввода "стоп" программа должна вывести весь словарь синонимов в формате: "слово: синоним".
synonyms = {}
while True:
    word = input("Введите слово (или 'стоп' для завершения): ")
    if word.lower() == "стоп":
        break
    synonym = input(f"Введите синоним для '{word}': ")
    synonyms[word] = synonym
print("Словарь синонимов:")
for word, synonym in synonyms.items():
    print(f"{word}: {synonym}")
